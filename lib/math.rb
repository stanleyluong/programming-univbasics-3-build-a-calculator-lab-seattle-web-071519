def addition(num1, num2)
  addition = num1 + num2
  p addition
end

def subtraction(num1, num2)
  subtraction = num1 - num2
  p subtraction
end

def division(num1, num2)
  division = num1 / num2
  p division
end

def multiplication(num1, num2)
  multiplication = num1 / num2
  p multiplication
end

def modulo(num1, num2)
  modulo = num1 % num2
  p modulo
end

def square_root(num)
  square_root = 1/(num**2)
  p square_root
end
